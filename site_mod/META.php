*/

function META($e) {
    if('<'==$e[0]) {
	$e=trim($e,'<>');
    } else {
	list($n,$v)=explode(" ",$e,2);
//	$GLOBALS['_META'][]='<meta name="'.h(c($n)).'" content="'.h(c($v)).'">';
	$e='meta name="'.h(c($n)).'" content="'.h(c($v)).'"';
    }
    $GLOBALS['_HEADD'][$e]=$e;
    return '';
}
